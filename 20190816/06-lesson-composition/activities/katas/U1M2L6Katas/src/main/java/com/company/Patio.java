package com.company;

public class Patio {

    private String color;
    private String floorType;
    private double deckSize;
    private String furnitureBrand;

    public int length(int a){
        return a;
    }

    public int width(int a){
        return a;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getFloorType() {
        return floorType;
    }

    public void setFloorType(String floorType) {
        this.floorType = floorType;
    }

    public double getDeckSize() {
        return deckSize;
    }

    public void setDeckSize(double deckSize) {
        this.deckSize = deckSize;
    }

    public String getFurnitureBrand() {
        return furnitureBrand;
    }

    public void setFurnitureBrand(String furnitureBrand) {
        this.furnitureBrand = furnitureBrand;
    }
}
